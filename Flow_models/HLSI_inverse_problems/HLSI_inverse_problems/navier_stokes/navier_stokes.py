class GaussianPrior(torch.nn.Module):
    def __init__(self, dim):
        super().__init__()
        self.dim = dim

    def sample(self, n):
        return torch.randn(n, self.dim, device=device)

    def log_prob(self, x):
        # log N(0, I)
        return -0.5 * torch.sum(x**2, dim=1) - (self.dim/2.0) * math.log(2*math.pi)

    def score0(self, x):
        # Score of N(0, I) is -x
        return -x

