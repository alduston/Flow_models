def reconstruct_raw_field(latents):
    """Map latent coefficients -> raw field m(x) on the (N, N) grid."""
    if isinstance(latents, torch.Tensor):
        latents = latents.detach().cpu().numpy()
    latents = np.asarray(latents)
    if latents.ndim == 1:
        latents = latents[None, :]
    d_lat = latents.shape[1]
    if Basis_np.shape[1] < d_lat:
        raise ValueError(
            f"Basis has only {Basis_np.shape[1]} columns but latents have dim {d_lat}."
        )
    B = Basis_np[:, :d_lat]
    fields_flat = latents @ B.T
    grid_size = int(np.sqrt(fields_flat.shape[1]))
    return fields_flat.reshape(latents.shape[0], grid_size, grid_size)


def raw_to_coefficient(raw_field):
    return Q_FLOOR + np.log1p(np.exp(np.asarray(raw_field)))


def reconstruct_coefficient_field(latents):
    raw = reconstruct_raw_field(latents)
    return raw_to_coefficient(raw)


