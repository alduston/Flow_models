def circular_smooth_1d(y, window=5):
    """Light circular moving-average smoothing for periodic boundary traces."""
    y = np.asarray(y)
    if window is None or int(window) <= 1 or y.size < 3:
        return y.copy()
    window = int(window)
    if window % 2 == 0:
        window += 1
    window = min(window, y.size if y.size % 2 == 1 else y.size - 1)
    if window <= 1:
        return y.copy()
    pad = window // 2
    kernel = np.ones(window, dtype=np.float64) / float(window)
    y_pad = np.concatenate([y[-pad:], y, y[:pad]])
    return np.convolve(y_pad, kernel, mode='valid')


