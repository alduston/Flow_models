# A. Define JAX Likelihood Functions

@jax.jit
def helmholtz_log_likelihood_jax(alpha_k, y_obs_jax, sigma):
    y_pred = solve_forward(alpha_k)
    resid = y_pred - y_obs_jax
    return -jnp.sum(resid ** 2) / (2.0 * sigma ** 2)

helmholtz_grad_lik_jax = jax.jit(jax.grad(helmholtz_log_likelihood_jax, argnums=0))
helmholtz_loglik_and_grad_jax = jax.jit(
    jax.value_and_grad(helmholtz_log_likelihood_jax, argnums=0)
)

USE_GAUSS_NEWTON_HESSIAN = True

solve_forward_jac_jax = jax.jit(jax.jacfwd(solve_forward))

@jax.jit
def helmholtz_hess_lik_gn_jax(alpha_k, y_obs_jax, sigma):
    J = solve_forward_jac_jax(alpha_k)
    return -(J.T @ J) / (sigma ** 2)

helmholtz_hess_lik_exact_jax = jax.jit(jax.hessian(helmholtz_log_likelihood_jax, argnums=0))

def helmholtz_hess_lik_jax(alpha_k, y_obs_jax, sigma):
    if USE_GAUSS_NEWTON_HESSIAN:
        return helmholtz_hess_lik_gn_jax(alpha_k, y_obs_jax, sigma)
    return helmholtz_hess_lik_exact_jax(alpha_k, y_obs_jax, sigma)

batch_log_lik = jax.vmap(helmholtz_log_likelihood_jax, in_axes=(0, None, None))
batch_grad_lik = jax.vmap(helmholtz_grad_lik_jax, in_axes=(0, None, None))
batch_loglik_and_grad = jax.vmap(helmholtz_loglik_and_grad_jax, in_axes=(0, None, None))
batch_hess_lik = jax.vmap(helmholtz_hess_lik_jax, in_axes=(0, None, None))

def batch_hess_lik_python(x_np, y_obs_jax, sigma):
    return np.stack(
        [np.asarray(helmholtz_hess_lik_jax(x_np[i], y_obs_jax, sigma))
         for i in range(x_np.shape[0])],
        axis=0,
    )

class PhysicsLikelihood:
    def __init__(self, y_obs_np, sigma, log_batch_size=50, grad_batch_size=25, hess_batch_size=2):
        self.y_obs_jax = jnp.array(y_obs_np)
        self.sigma = float(sigma)
        self.log_batch_size = int(log_batch_size)
        self.grad_batch_size = int(grad_batch_size)
        self.hess_batch_size = int(hess_batch_size)

    def _to_numpy_batch(self, x_torch):
        x_np = np.asarray(x_torch.detach().cpu().numpy(), dtype=np.float64)
        if x_np.ndim == 1:
            x_np = x_np[None, :]
        return x_np

    def _chunked_eval(self, x_torch, fn, batch_size):
        x_np = self._to_numpy_batch(x_torch)
        outs = []
        for i in range(0, x_np.shape[0], batch_size):
            outs.append(np.asarray(fn(x_np[i:i + batch_size], self.y_obs_jax, self.sigma)))
        out_np = np.concatenate(outs, axis=0)
        return torch.tensor(out_np, device=x_torch.device, dtype=torch.float64)

    def log_likelihood(self, x_torch, batch_size=None):
        if batch_size is None:
            batch_size = self.log_batch_size
        return self._chunked_eval(x_torch, batch_log_lik, batch_size)

    def grad_log_likelihood(self, x_torch, batch_size=None):
        if batch_size is None:
            batch_size = self.grad_batch_size
        return self._chunked_eval(x_torch, batch_grad_lik, batch_size)

    def log_likelihood_and_grad(self, x_torch, batch_size=None):
        if batch_size is None:
            batch_size = min(self.log_batch_size, self.grad_batch_size)
        x_np = self._to_numpy_batch(x_torch)
        ll_list = []
        grad_list = []
        for i in range(0, x_np.shape[0], batch_size):
            ll_chunk, grad_chunk = batch_loglik_and_grad(x_np[i:i + batch_size], self.y_obs_jax, self.sigma)
            ll_list.append(np.asarray(ll_chunk))
            grad_list.append(np.asarray(grad_chunk))
        ll_np = np.concatenate(ll_list, axis=0)
        grad_np = np.concatenate(grad_list, axis=0)
        return (
            torch.tensor(ll_np, device=x_torch.device, dtype=torch.float64),
            torch.tensor(grad_np, device=x_torch.device, dtype=torch.float64),
        )

    def hess_log_likelihood(self, x_torch, batch_size=None):
        if batch_size is None:
            batch_size = self.hess_batch_size
        x_np = self._to_numpy_batch(x_torch)
        hess_list = []
        for i in range(0, x_np.shape[0], batch_size):
            hess_list.append(batch_hess_lik_python(x_np[i:i + batch_size], self.y_obs_jax, self.sigma))
        hess_np = np.concatenate(hess_list, axis=0)
        return torch.tensor(hess_np, device=x_torch.device, dtype=torch.float64)

