@partial(jax.jit, static_argnums=(4,))
def sp_matmul(values_, rows_, cols_, B, shape):
    """Sparse matrix-vector multiply compatible with JAX JIT.

    Computes A @ B where the sparse matrix A is encoded by (values, rows, cols).
    """
    B = jnp.expand_dims(B, axis=1)
    gathered = B.take(cols_, axis=0)
    prod = gathered * values_[:, None]
    return jax.ops.segment_sum(prod, rows_, shape)


@jax.jit
def solve_forward(alpha_full):
    """Forward map F(alpha_full) for the inverse heat equation.

    alpha_full lives in the FULL_DIM-dimensional KL coefficient space.
    The conductivity/log-field is built exactly as in heat_solve_lsi.py.
    """
    parameter_of_interest = jnp.dot(alpha_full, jnp.transpose(jnp.dot(Eigen, Sigma)))

    stiff = sp_matmul(values, rows, cols, jnp.exp(parameter_of_interest), DIMENSION_OF_POI ** 2)
    stiff = jnp.reshape(stiff, (DIMENSION_OF_POI, DIMENSION_OF_POI))

    A = jnp.squeeze(jnp.take(stiff, free_index_jax, axis=1))
    B = jnp.squeeze(jnp.take(A, free_index_jax, axis=0))

    state = jnp.linalg.solve(B, load_vector)
    out_pred = jnp.dot(obs_operator, state).squeeze()
    return out_pred


