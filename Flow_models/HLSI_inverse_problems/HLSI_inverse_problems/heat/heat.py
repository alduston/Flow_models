    print("Visualizing conductivity fields and error maps...")
    methods_to_plot = [label for label in samples_heat.keys() if label in mean_fields]
    n_cols = len(methods_to_plot) + 1
    fig, axes = plt.subplots(4, n_cols, figsize=(4 * n_cols, 14))

    # Use the ground-truth field to set the color normalization for the
    # conductivity visualizations (top and bottom rows). This avoids the plot
    # scale being distorted when a sampler like MALA fails or collapses.
    vmin = float(true_field.min())
    vmax = float(true_field.max())

    vis_anchor_key = reference_key if reference_key in mean_fields else next(iter(mean_fields.keys()))
    if vis_anchor_key in samples_heat and vis_anchor_key in mean_fields:
        anchor_vis_samps = get_valid_samples(samples_heat[vis_anchor_key])[:1000]
        if anchor_vis_samps.shape[0] > 0:
            anchor_vis_fields = reconstruct_field(anchor_vis_samps[:, :ACTIVE_DIM])
            max_err = max(1e-12, float(np.abs(mean_fields[vis_anchor_key] - true_field).max()))
            max_std = max(1e-12, float(np.std(anchor_vis_fields, axis=0).max()))
        else:
            max_err, max_std = 1e-12, 1e-12
    else:
        max_err, max_std = 1e-12, 1e-12

    im0 = axes[0, 0].imshow(true_field, cmap="RdBu_r", origin="lower", vmin=vmin, vmax=vmax, interpolation="bicubic")
    axes[0, 0].set_title("Ground Truth Log-Conductivity", fontsize=18)
    axes[0, 0].axis("off")
    plt.colorbar(im0, ax=axes[0, 0], fraction=0.046, pad=0.04)

    axes[3, 0].imshow(true_field, cmap="RdBu_r", origin="lower", vmin=vmin, vmax=vmax, interpolation="bicubic")
    axes[3, 0].set_title("Ground Truth", fontsize=14)
    axes[3, 0].axis("off")
    axes[1, 0].axis("off")
    axes[2, 0].axis("off")

    if vis_anchor_key not in mean_fields:
        for label in methods_to_plot:
            mean_f = mean_fields[label]
            max_err = max(max_err, np.abs(mean_f - true_field).max())
            samps = get_valid_samples(samples_heat[label])[:500]
            if samps.shape[0] > 0:
                fields = reconstruct_field(samps[:, :ACTIVE_DIM])
                max_std = max(max_std, np.std(fields, axis=0).max())

    for i, label in enumerate(methods_to_plot):
        col = i + 1
        mean_f = mean_fields[label]

        axes[0, col].imshow(mean_f, cmap="RdBu_r", origin="lower", vmin=vmin, vmax=vmax, interpolation="bicubic")
        axes[0, col].set_title(f"{display_names.get(label, label)} Mean Posterior", fontsize=18)
        axes[0, col].axis("off")

        err_f = np.abs(mean_f - true_field)
        axes[1, col].imshow(err_f, cmap="inferno", origin="lower", vmin=0, vmax=max_err, interpolation="bicubic")
        axes[1, col].set_title(f"Error Map(Max: {err_f.max():.2f})", fontsize=16)
        axes[1, col].axis("off")

        samps = get_valid_samples(samples_heat[label])[:1000]
        if samps.shape[0] > 0:
            fields = reconstruct_field(samps[:, :ACTIVE_DIM])
            std_f = np.std(fields, axis=0)
        else:
            fields = None
            std_f = np.zeros_like(true_field)

        im_std = axes[2, col].imshow(std_f, cmap="viridis", origin="lower", vmin=0, vmax=max_std, interpolation="bicubic")
        axes[2, col].set_title(r"Uncertainty $\sigma$", fontsize=16)
        axes[2, col].axis("off")
        plt.colorbar(im_std, ax=axes[2, col], fraction=0.046, pad=0.04)

        if fields is not None and samps.shape[0] > 0:
            samp_f = fields[-1]
            im_samp = axes[3, col].imshow(samp_f, cmap="RdBu_r", origin="lower", vmin=vmin, vmax=vmax, interpolation="bicubic")
            axes[3, col].set_title("Random Sample", fontsize=14)
            axes[3, col].axis("off")
            plt.colorbar(im_samp, ax=axes[3, col], fraction=0.046, pad=0.04)
        else:
            axes[3, col].axis("off")

    plt.suptitle(f"Inverse Heat Equation (k={ACTIVE_DIM}): Conductivity Reconstruction", fontsize=18, y=1.02)
    plt.tight_layout()
    if save_path is not None:
        fig.savefig(save_path, dpi=180, bbox_inches="tight")
    plt.show()


