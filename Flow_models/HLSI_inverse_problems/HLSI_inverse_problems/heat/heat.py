@jax.jit
def pad_coefficients(alpha_k):
    if ACTIVE_DIM == FULL_DIM:
        return alpha_k
    padding = jnp.zeros(FULL_DIM - ACTIVE_DIM, dtype=alpha_k.dtype)
    return jnp.concatenate([alpha_k, padding], axis=0)


@jax.jit
def heat_forward_Nd(alpha_k):
    alpha_full = pad_coefficients(alpha_k)
    return solve_forward(alpha_full)


@jax.jit
def heat_log_likelihood_jax(alpha_k, y_obs_jax, sigma):
    y_pred = heat_forward_Nd(alpha_k)
    resid = y_pred - y_obs_jax
    return -jnp.sum(resid ** 2) / (2.0 * sigma ** 2)


heat_grad_lik_jax = jax.jit(jax.grad(heat_log_likelihood_jax, argnums=0))
heat_hess_lik_exact_jax = jax.jit(jax.hessian(heat_log_likelihood_jax, argnums=0))

# Jacobian of forward map for Gauss-Newton Hessian approximation
heat_forward_jac_jax = jax.jit(jax.jacfwd(heat_forward_Nd))


@jax.jit
def heat_hess_lik_gn_jax(alpha_k, y_obs_jax, sigma):
    """Gauss-Newton Hessian of log-likelihood:
    log L = -||F(x)-y||^2 / (2 sigma^2)  =>  Hess(log L) approx -(J^T J) / sigma^2
    """
    J = heat_forward_jac_jax(alpha_k)   # shape: (m, d)
    return -(J.T @ J) / (sigma ** 2)


def heat_hess_lik_jax(alpha_k, y_obs_jax, sigma):
    if USE_GAUSS_NEWTON_HESSIAN:
        return heat_hess_lik_gn_jax(alpha_k, y_obs_jax, sigma)
    return heat_hess_lik_exact_jax(alpha_k, y_obs_jax, sigma)


batch_log_lik = jax.jit(jax.vmap(heat_log_likelihood_jax, in_axes=(0, None, None)))
batch_grad_lik = jax.jit(jax.vmap(heat_grad_lik_jax, in_axes=(0, None, None)))
batch_hess_lik = jax.vmap(heat_hess_lik_jax, in_axes=(0, None, None))


class HeatEquationLikelihood:
    def __init__(self, y_obs_np, sigma):
        self.y_obs_jax = jnp.array(y_obs_np)
        self.sigma = float(sigma)

    def log_likelihood(self, x_torch: torch.Tensor) -> torch.Tensor:
        x_np = x_torch.detach().cpu().numpy()
        ll_jax = batch_log_lik(x_np, self.y_obs_jax, self.sigma)
        return torch.tensor(np.array(ll_jax).copy(), device=x_torch.device, dtype=torch.float64)

    def grad_log_likelihood(self, x_torch: torch.Tensor) -> torch.Tensor:
        x_np = x_torch.detach().cpu().numpy()
        grad_jax = batch_grad_lik(x_np, self.y_obs_jax, self.sigma)
        return torch.tensor(np.array(grad_jax).copy(), device=x_torch.device, dtype=torch.float64)

    def hess_log_likelihood(self, x_torch: torch.Tensor) -> torch.Tensor:
        x_np = x_torch.detach().cpu().numpy()
        hess_jax = batch_hess_lik(x_np, self.y_obs_jax, self.sigma)
        return torch.tensor(np.array(hess_jax).copy(), device=x_torch.device, dtype=torch.float64)


class GaussianPrior(torch.nn.Module):
    def __init__(self, dim: int):
        super().__init__()
        self.dim = dim

    def sample(self, n: int) -> torch.Tensor:
        return torch.randn(n, self.dim, device=DEVICE, dtype=torch.float64)

    def log_prob(self, x: torch.Tensor) -> torch.Tensor:
        return -0.5 * torch.sum(x ** 2, dim=1) - (self.dim / 2.0) * math.log(2.0 * math.pi)

    def score0(self, x: torch.Tensor) -> torch.Tensor:
        return -x


