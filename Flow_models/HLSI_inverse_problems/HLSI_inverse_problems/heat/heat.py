def run_heat_inverse_problem(
    sampler_configs=SAMPLER_CONFIGS_HEAT,
    out_dir=OUT_DIR,
    seed=42,
    n_ref=N_REF_HEAT,
    hess_batch_size=HESS_BATCH_SIZE,
):
    os.makedirs(out_dir, exist_ok=True)

    print(f"--- Setting up {ACTIVE_DIM}D heat-equation inverse problem ---")

    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()
    if hasattr(jax, "clear_caches"):
        jax.clear_caches()
    _HLSI_CACHE.clear()

    np.random.seed(seed)
    alpha_true_np = 0.5 * np.random.randn(ACTIVE_DIM)
    y_clean = np.array(heat_forward_Nd(jnp.array(alpha_true_np)))
    y_obs_np = y_clean + np.random.normal(0.0, NOISE_STD, size=y_clean.shape)

    prior_model_heat = GaussianPrior(dim=ACTIVE_DIM)
    lik_model_heat = HeatEquationLikelihood(y_obs_np, NOISE_STD)

    print(f"Generating {n_ref} prior reference samples...")
    t0 = time.time()
    X_ref_heat = prior_model_heat.sample(n_ref)
    base_bank = precompute_reference_bank(
        X_ref_heat,
        prior_model_heat,
        lik_model_heat,
        label='heat',
        compute_pou=True,
    )
    PRECOMP_HEAT = {'base': base_bank, **base_bank}
    print(f"Reference bank precomputation: {time.time() - t0:.2f}s")

    samples_heat, ess_logs_heat, sampler_run_info_heat = run_sampler_suite(
        sampler_configs,
        prior_model_heat,
        lik_model_heat,
        PRECOMP_HEAT,
    )

    display_names_heat = {label: info.get("display_name", label) for label, info in sampler_run_info_heat.items()}
    reference_key_heat = choose_reference_key(samples_heat, sampler_run_info_heat)
    reference_title_heat = display_names_heat.get(reference_key_heat, reference_key_heat)

    print("\n=== Mean ESS vs t (across particles) ===")
    plot_mean_ess_logs(
        ess_logs_heat,
        display_names=display_names_heat,
    )

    print("\n=== Evaluation (latent / coefficient metrics) ===")
    print(
        f"{'Method':<24} | {'RMSE_alpha':<10} | {'RelL2_alpha':<11} | "
        f"{('MMD->' + reference_title_heat)[:14]:<14} | {'KSD':<10} | {'KLdiag':<10}"
    )
    print("-" * 95)

    ref_clean = robust_clean_samples(samples_heat[reference_key_heat])
    alpha_true_t = torch.tensor(alpha_true_np, device=DEVICE, dtype=torch.float64)
    metrics_heat = {}

    for label, raw in samples_heat.items():
        samps = robust_clean_samples(raw)
        if samps.shape[0] < 50:
            continue

        mean_latent = torch.mean(samps, dim=0)
        rmse_alpha = rmse_vec(mean_latent, alpha_true_t)
        rel_alpha = rel_l2_vec(mean_latent, alpha_true_t)
        mmd = compute_mmd_rbf(samps, ref_clean)
        ksd = compute_multiscale_ksd(samps, lambda x: posterior_score_fn(x, lik_model_heat))
        kl = compute_kl_divergence(samps, prior_model_heat, lik_model_heat)

        metrics_heat[label] = dict(
            mean_latent=mean_latent.detach().cpu().numpy(),
            RMSE_alpha=rmse_alpha,
            RelL2_alpha=rel_alpha,
            MMD_to_reference=mmd,
            KSD=ksd,
            KLdiag=kl,
        )

        print(
            f"{display_names_heat.get(label, label):<24} | {rmse_alpha:<10.4f} | {rel_alpha:<11.4f} | "
            f"{mmd:<14.4f} | {ksd:<10.4f} | {kl:<10.4f}"
        )

    print("\n=== PCA Histograms ===")
    plot_pca_histograms(
        samples_heat,
        alpha_true_np,
        display_names=display_names_heat,
        reference_key=reference_key_heat,
    )

    # Heat-specific physical metrics / visualizations.
    true_field = reconstruct_field(alpha_true_np)[0]
    norm_true = np.linalg.norm(true_field) + 1e-12
    norm_y_clean = np.linalg.norm(y_clean) + 1e-12

    print("\n=== Physical Parameter Space Metrics (Conductivity field + forward error) ===")
    print(f"{'Method':<24} | {'Inv RelL2(%)':<12} | {'RMSE_alpha':<12} | {'FwdRelErr':<12}")
    print("-" * 76)

    mean_fields = {}
    for label, samps in samples_heat.items():
        samps_clean = get_valid_samples(samps)
        if samps_clean.shape[0] < 10:
            print(f"{display_names_heat.get(label, label):<24} | FAILED (Unstable / too few valid)")
            continue

        mean_latent = np.mean(samps_clean, axis=0)[:ACTIVE_DIM]
        mean_field = reconstruct_field(mean_latent)[0]
        mean_fields[label] = mean_field

        inv_rel_l2_pct = 100.0 * (np.linalg.norm(mean_field - true_field) / norm_true)
        rmse_alpha = rmse_array(mean_latent, alpha_true_np)

        try:
            y_pred = np.array(heat_forward_Nd(jnp.array(mean_latent)))
            fwd_rel = float(np.linalg.norm(y_pred - y_clean) / norm_y_clean)
        except Exception:
            fwd_rel = float("nan")

        if label not in metrics_heat:
            metrics_heat[label] = {}
        metrics_heat[label].update(
            dict(
                mean_latent=mean_latent,
                RMSE_alpha=rmse_alpha,
                RMSE_field=rmse_array(mean_field, true_field),
                RelL2_field=float(np.linalg.norm(mean_field - true_field) / norm_true),
                FwdRelErr=fwd_rel,
            )
        )

        print(
            f"{display_names_heat.get(label, label):<24} | {inv_rel_l2_pct:<12.4f} | "
            f"{rmse_alpha:<12.4e} | {fwd_rel:<12.4e}"
        )

    print("\n=== Final heat metrics (paper-ready) ===")
    print(
        f"{'Method':<24} | {'RMSE_a':<9} | {'RMSE_u':<9} | {'FwdRel':<9} | "
        f"{('MMD->' + reference_title_heat)[:12]:<12} | {'KSD':<9} | {'KLdiag':<9}"
    )
    print("-" * 98)
    for label, data in metrics_heat.items():
        print(
            f"{display_names_heat.get(label, label):<24} | {data.get('RMSE_alpha', float('nan')):<9.3e} | "
            f"{data.get('RMSE_field', float('nan')):<9.3e} | {data.get('FwdRelErr', float('nan')):<9.3e} | "
            f"{data.get('MMD_to_reference', float('nan')):<12.3e} | {data.get('KSD', float('nan')):<9.3e} | "
            f"{data.get('KLdiag', float('nan')):<9.3e}"
        )

    results_df, results_runinfo_df = build_results_dataframes(
        metrics_heat,
        sampler_run_info_heat,
        display_names_heat,
        n_ref=n_ref,
        target_name="Heat equation conductivity field",
        reference_name=reference_title_heat,
    )

    results_df_path = os.path.join(out_dir, f"{RUN_RESULTS_STEM}_metrics.csv")
    results_runinfo_df_path = os.path.join(out_dir, f"{RUN_RESULTS_STEM}_runinfo.csv")
    results_df.to_csv(results_df_path)
    results_runinfo_df.to_csv(results_runinfo_df_path, index=False)

    print(f"\nSaved results dataframe to {results_df_path}")
    print(f"Saved run-info dataframe to {results_runinfo_df_path}")

    repro_log_path = save_reproducibility_log(
        extra_sections={
            "saved_results_files": {
                "metrics_csv": results_df_path,
                "runinfo_csv": results_runinfo_df_path,
            },
            "input_file_requirements": {
                "source_repository": "https://github.com/nguyenvanhaibk92/mcTagent_digital_twin",
                "note": "Required input files for this script are identified near the start of the script and should be obtained from the repository above.",
                "expected_files": [
                    "data/prior_mean_n15_AC_1_1_pt5.csv",
                    "data/prior_covariance_n15_AC_1_1_pt5.csv",
                    "data/poisson_2D_obs_indices_o10_n15.csv",
                    "data/prior_covariance_cholesky_n15_AC_1_1_pt5.csv",
                    "data/prestiffness_n15.npz",
                    "data/load_vector_n15.npz",
                ],
            },
            "summary_stats": {
                "reference_key": reference_key_heat,
                "reference_title": reference_title_heat,
                "num_methods_evaluated": len(results_df.columns),
                "num_methods_with_samples": len(samples_heat),
                "num_methods_with_mean_fields": len(mean_fields),
            },
        }
    )
    print(f"Saved reproducibility log to {repro_log_path}")

    plot_heat_field_panels(
        samples_heat,
        mean_fields,
        true_field,
        display_names_heat,
        reference_key_heat,
    )

    run_results_zip_path = zip_run_results_dir()
    print(f"Run-results directory: {RUN_RESULTS_DIR}")
    print(f"Run-results zip: {run_results_zip_path}")


    return {
        "alpha_true": alpha_true_np,
        "y_clean": y_clean,
        "y_obs": y_obs_np,
        "prior_model": prior_model_heat,
        "lik_model": lik_model_heat,
        "precomp": PRECOMP_HEAT,
        "samples": samples_heat,
        "ess_logs": ess_logs_heat,
        "run_info": sampler_run_info_heat,
        "metrics": metrics_heat,
        "display_names": display_names_heat,
        "reference_key": reference_key_heat,
        "out_dir": out_dir,
        "results_df_path": results_df_path,
        "results_runinfo_df_path": results_runinfo_df_path,
        "repro_log_path": repro_log_path,
        "run_results_zip_path": run_results_zip_path,
    }


if __name__ == "__main__":
    RESULTS_HEAT = run_heat_inverse_problem()

