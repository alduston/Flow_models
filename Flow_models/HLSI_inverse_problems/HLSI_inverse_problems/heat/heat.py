def compute_hessians_batched(lik_model, samples, batch_size=200):
    N_samp, dim = samples.shape
    num_batches = int(np.ceil(N_samp / batch_size))
    hessians = []
    print(f"Computing Hessians for {N_samp} samples in {num_batches} batches...")
    t0 = time.time()
    for i in range(num_batches):
        start = i * batch_size
        end = min((i + 1) * batch_size, N_samp)
        batch_slice = samples[start:end]
        batch_hess = lik_model.hess_log_likelihood(batch_slice)
        hessians.append(batch_hess.cpu())
        if (i + 1) % 5 == 0 or i == num_batches - 1:
            print(f"  Batch {i + 1}/{num_batches} done.")
    print(f"Hessian computation took {time.time() - t0:.2f}s")
    return torch.cat(hessians, dim=0)


def reconstruct_field(latents_k_dim):
    if isinstance(latents_k_dim, torch.Tensor):
        latents_k_dim = latents_k_dim.detach().cpu().numpy()
    latents_k_dim = np.asarray(latents_k_dim)
    if latents_k_dim.ndim == 1:
        latents_k_dim = latents_k_dim[None, :]

    N_samp, k = latents_k_dim.shape
    full_dim = int(np.array(Sigma).shape[0])
    if k > full_dim:
        raise ValueError(f"Latent dimension {k} exceeds available full dimension {full_dim}.")

    padding = np.zeros((N_samp, full_dim - k), dtype=np.float64)
    latents_full = np.hstack([latents_k_dim, padding])
    transform_matrix = np.dot(np.array(Eigen), np.array(Sigma))
    fields_flat = np.dot(latents_full, transform_matrix.T)
    grid = int(np.sqrt(fields_flat.shape[1]))
    return fields_flat.reshape(N_samp, grid, grid)


def get_valid_samples(samps):
    if isinstance(samps, torch.Tensor):
        samps = samps.detach().cpu().numpy()
    samps = np.asarray(samps)
    valid = samps[np.isfinite(samps).all(axis=1)]
    if len(valid) < 10:
        return valid
    q25 = np.percentile(valid, 25, axis=0)
    q75 = np.percentile(valid, 75, axis=0)
    iqr = q75 - q25
    mask = ((valid >= q25 - 5 * iqr) & (valid <= q75 + 5 * iqr)).all(axis=1)
    return valid[mask]


def rmse_array(a_hat, a_true):
    return float(np.sqrt(np.mean((np.asarray(a_hat) - np.asarray(a_true)) ** 2)))


